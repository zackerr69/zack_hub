-- Функция для радужного текста
local function makeRainbow(button)
    spawn(function()
        local hue = 0
        while button and button.Parent do
            hue = (hue + 0.01) % 1
            button.TextColor3 = Color3.fromHSV(hue, 0.8, 1)
            wait(0.05)
        end
    end)
end

